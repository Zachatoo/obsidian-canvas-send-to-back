import esbuild from "esbuild";
import process from "process";
import { builtinModules } from "node:module";
import { readFileSync } from "fs";
import { exec } from "child_process";

let manifest = JSON.parse(readFileSync("manifest.json", "utf8"));

const reloadObsidianPlugin = {
	name: "reload-obsidian-plugin",
	setup(build) {
		build.onEnd(() => {
			exec(
				`obsidian plugin:reload id=${manifest.id}`,
				(error, stdout, stderr) => {
					if (error) {
						console.error(
							`[reload-obsidian-plugin] Error reloading plugin: ${error.message}`,
						);
						return;
					}
					if (stderr) {
						console.error(
							`[reload-obsidian-plugin] Error reloading plugin: ${stderr}`,
						);
						return;
					}
					console.log(
						`[reload-obsidian-plugin] Plugin reloaded: ${manifest.id}`,
					);
				},
			);
		});
	},
};

const banner = `/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
if you want to view the source, please visit the github repository of this plugin
*/
`;

const prod = process.argv[2] === "production";

const context = await esbuild.context({
	banner: {
		js: banner,
	},
	plugins: [reloadObsidianPlugin],
	entryPoints: ["src/main.ts"],
	bundle: true,
	external: [
		"obsidian",
		"electron",
		"@codemirror/autocomplete",
		"@codemirror/collab",
		"@codemirror/commands",
		"@codemirror/language",
		"@codemirror/lint",
		"@codemirror/search",
		"@codemirror/state",
		"@codemirror/view",
		"@lezer/common",
		"@lezer/highlight",
		"@lezer/lr",
		...builtinModules,
	],
	format: "cjs",
	target: "es2018",
	logLevel: "info",
	sourcemap: prod ? false : "inline",
	treeShaking: true,
	outfile: "main.js",
});

if (prod) {
	await context.rebuild();
	process.exit(0);
} else {
	await context.watch();
}
